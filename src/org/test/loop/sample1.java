package org.test.loop;

public class sample1 {

	public static void main(String[] args) {
		System.out.println("using while loop");
		int j=10;
		while(j<5) {
			System.out.println(j);
			j--;
		}
		System.out.println("using for loop");
		for(int i=0;i<=5;i++) {
			System.out.println(i);

	}

}
	
}
